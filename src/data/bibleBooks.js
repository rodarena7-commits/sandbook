import { bibleBooks } from './data/bibleBooks';

// Estado para la Biblia
const [bibleData] = useState(bibleBooks);
const [showBiblePlan, setShowBiblePlan] = useState(false);
const [selectedBibleBook, setSelectedBibleBook] = useState(null);

// Función para seleccionar un libro y mostrar sus capítulos
const selectBibleBook = (book) => {
  setSelectedBibleBook(book);
};

// Función para seleccionar un capítulo específico y crear un plan
const selectBibleChapter = (book, chapter) => {
  const bibleChapterAsPlanningBook = {
    id: `bible-${book.name.replace(/\s+/g, '-').toLowerCase()}-cap${chapter}`,
    title: `La Biblia: ${book.name} - Capítulo ${chapter}`,
    volumeInfo: {
      title: `La Biblia: ${book.name} - Capítulo ${chapter}`,
      pageCount: 1 // Un capítulo = 1 "página" en tu sistema de planificación
    }
  };
  
  setPlanningBook(bibleChapterAsPlanningBook);
  setManualPages("1");
};

// JSX para mostrar la Biblia
{showBiblePlan && (
  <div className={`${themeClasses.card} p-6 rounded-3xl border ${themeClasses.border} max-h-96 overflow-y-auto`}>
    <h3 className="font-black text-lg mb-4 flex items-center gap-2">
      <img src="/biblia.png" alt="Biblia" className="w-8 h-8" />
      {selectedBibleBook ? `${selectedBibleBook.name} - Capítulos` : 'Libros de la Biblia (66)'}
    </h3>
    
    {selectedBibleBook ? (
      <>
        <button 
          onClick={() => setSelectedBibleBook(null)}
          className="mb-4 text-sm text-indigo-600 flex items-center gap-1"
        >
          ← Volver a libros
        </button>
        <div className="grid grid-cols-5 gap-2">
          {selectedBibleBook.chapters.map(chapter => (
            <button
              key={chapter}
              onClick={() => selectBibleChapter(selectedBibleBook, chapter)}
              className="p-2 bg-slate-100 dark:bg-gray-700 hover:bg-indigo-100 dark:hover:bg-indigo-900 rounded-lg text-center font-bold text-sm transition-all"
            >
              {chapter}
            </button>
          ))}
        </div>
      </>
    ) : (
      <div className="space-y-2">
        {bibleData.map((book, index) => (
          <div
            key={index}
            className="flex items-center justify-between p-3 rounded-2xl bg-slate-50 dark:bg-gray-700 hover:bg-slate-100 dark:hover:bg-gray-600 cursor-pointer transition-all"
            onClick={() => selectBibleBook(book)}
          >
            <div className="flex items-center gap-3">
              <span className="text-xs font-bold text-indigo-600 dark:text-indigo-400 w-6">{index + 1}</span>
              <span className="font-bold text-sm">{book.name}</span>
            </div>
            <span className="text-xs text-slate-500 dark:text-gray-400">
              {book.chapters.length} capítulos
            </span>
          </div>
        ))}
      </div>
    )}
  </div>
)}
